void main(){
  var x=5;
  var y=9;
  print("${x} > ${y} is: ${x > y}");
  print("-----------------------------------------------");
  print("${x} < ${y} is: ${x < y}");
  print("-----------------------------------------------");
  print("${x} >= ${y} is: ${x >= y}");
  print("-----------------------------------------------");
  print("${x} <= ${y} is: ${x <= y}");
  print("-----------------------------------------------");
  print("${x} == ${y} is: ${x == y}");
  print("-----------------------------------------------");
  print("${x} != ${y} is: ${x != y}");
  print("-----------------------------------------------");
}