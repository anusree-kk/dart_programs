  print("-----------------------------------------------");
  print("${x} %=	${y} is: ${x %= y}");
  print("-----------------------------------------------");
  print("${x} +=	${y} is: ${x += y}");
  print("-----------------------------------------------");
  print("${x} *=	${y} is: ${x *= y}");
  print("-----------------------------------------------");
  print("${x} ~/=	${y} is: ${x ~/ y}");
  print("-----------------------------------------------");
  print("${x} >>=	${y} is: ${x >>= y}");
  print("-----------------------------------------------");
  print("${x} ^=	${y} is: ${x ^= y}");
  print("-----------------------------------------------");
  print("${x} <<=	${y} is: ${x != y}");
  print("-----------------------------------------------");


}